namespace DSharpPlus.Core.RestEntities
{
    public static class DiscordGuildFeature
    {
        /// <summary>
        /// The guild has access to set an animated guild banner image.
        /// </summary>
        public const string AnimatedBanner = "ANIMATED_BANNER";

        /// <summary>
        /// The guild has access to set an animated guild icon.
        /// </summary>
        public const string AnimatedIcon = "ANIMATED_ICON";

        /// <summary>
        /// The guild has access to set a guild banner image.
        /// </summary>
        public const string Banner = "BANNER";

        /// <summary>
        /// The guild has access to use commerce features (i.e. create store channels).
        /// </summary>
        public const string Commerce = "COMMERCE";

        /// <summary>
        /// The guild can enable welcome screen, Membership Screening, stage channels and discovery, and receives community updates.
        /// </summary>
        public const string Community = "COMMUNITY";

        /// <summary>
        /// The guild is able to be discovered in the directory.
        /// </summary>
        public const string Discoverable = "DISCOVERABLE";

        /// <summary>
        /// The guild is able to be featured in the directory.
        /// </summary>
        public const string Featurable = "FEATURABLE";

        /// <summary>
        /// The guild has access to set an invite splash background.
        /// </summary>
        public const string InviteSplash = "INVITE_SPLASH";

        /// <summary>
        /// The guild has enabled <see cref="DiscordGuildMembershipScreen"/> (Membership Screening).
        /// </summary>
        public const string MemberVerificationGateEnabled = "MEMBER_VERIFICATION_GATE_ENABLED";

        /// <summary>
        /// The guild has enabled monetization.
        /// </summary>
        public const string MonetizationEnabled = "MONETIZATION_ENABLED";

        /// <summary>
        /// The guild has increased custom sticker slots.
        /// </summary>
        public const string MoreStickers = "MORE_STICKERS";

        /// <summary>
        /// The guild has access to create news channels.
        /// </summary>
        public const string News = "NEWS";

        /// <summary>
        /// The guild is partnered.
        /// </summary>
        public const string Partnered = "PARTNERED";

        /// <summary>
        /// The guild can be previewed before joining via Membership Screening or the directory.
        /// </summary>
        public const string PreviewEnabled = "PREVIEW_ENABLED";

        /// <summary>
        /// The guild has access to create private threads.
        /// </summary>
        public const string PrivateThreads = "PRIVATE_THREADS";

        /// <summary>
        /// The guild is able to set role icons.
        /// </summary>
        public const string RoleIcons = "ROLE_ICONS";

        /// <summary>
        /// The guild has enabled ticketed events.
        /// </summary>
        public const string TicketedEventsEnabled = "TICKETED_EVENTS_ENABLED";

        /// <summary>
        /// The guild has access to set a vanity URL.
        /// </summary>
        public const string VanityUrl = "VANITY_URL";

        /// <summary>
        /// The guild is verified.
        /// </summary>
        public const string Verified = "VERIFIED";

        /// <summary>
        /// The guild has access to set 384kbps bitrate in voice (previously VIP voice servers).
        /// </summary>
        public const string VipRegions = "VIP_REGIONS";

        /// <summary>
        /// The guild has enabled the welcome screen.
        /// </summary>
        public const string WelcomeScreenEnabled = "WELCOME_SCREEN_ENABLED";
    }
}
