namespace DSharpPlus.Entities;

/// <summary>
/// The sort order for forum channels.
/// </summary>
public enum DiscordDefaultSortOrder
{
    /// <summary>
    /// Sorts posts by the latest message in the thread.
    /// </summary>
    LatestActivity,
    /// <summary>
    /// Sorts posts by the creation of the post itself.
    /// </summary>
    CreationDate
}
