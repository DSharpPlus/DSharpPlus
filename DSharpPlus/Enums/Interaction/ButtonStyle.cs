namespace DSharpPlus
{
    /// <summary>
    /// Represents a button's style/color.
    /// </summary>
    public enum ButtonStyle : int
    {
        /// <summary>
        /// Blurple button.
        /// </summary>
        Primary = 1,
        /// <summary>
        /// Grey button.
        /// </summary>
        Secondary = 2,
        /// <summary>
        /// Green button.
        /// </summary>
        Success  = 3,
        /// <summary>
        /// Red button.
        /// </summary>
        Danger = 4,
    }
}
